#include <cs50.h>
#include <stdio.h>


int main() {
    // For loop to print numbers from 1 to 5
    for (int i = 1; i <= 5; i++) {
        printf("%d\n", i);
    }

 int i = 1; // Initialize the loop variable

    // While loop to print numbers from 1 to 5
    while (i <= 5) {
        printf("%d\n", i);
        i++; // Increment the loop variable
    }

    return 0;
}
